<p> <b>Friday, June 18, 10:15-12:00</b>, EA 60, Room N0.007 (Nebengebäude)
<br/>
<b>John Mumma</b>, Euclid’s diagrams and the primitive concepts of elementary geometry
<br/>
Abstract: 
<i>
One thing a formalization of a mathematical theory provides is a sharp classification of the theory’s concepts as either primitive or defined.  In this talk, I examine how this separation is made in two formalizations of Euclid’s elementary plane geometry:  the axiomatization presented in Tarski’s `What is elementary geometry?’, and the formal system E.  The latter is a system that has recently been developed to illuminate the role of diagrams in the proofs in the Elements.  In accounting for Euclid’s diagrams, E classifies more geometric relations as primitive than Tarski’s formalization does.  At the same time, the logic of E is much weaker.  I show what this trade-off amounts to precisely by describing E as a formal system and sketching the proof that E is complete relative to Tarksi’s theory.
</i>

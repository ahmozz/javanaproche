<h3>Das Naproche-Projekt </h3>
<p>
Das Naproche-Projekt untersucht den Zusammenhang zwischen formalen und informellen Beweisen. Ein Hauptbestandteil unserer Arbeit ist die Entwicklung des Naproche-Systems. Dabei handelt es sich um ein Computer-System, das natürlichsprachliche mathematische Texte auf logische Korrektheit überprüft.
</p>

